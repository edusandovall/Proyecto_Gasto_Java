    public void listarActividades()
    {
       try 
       {
           DefaultTableModel table = new DefaultTableModel();
           ArrayList<ActividadVO> listaActividades= new ArrayList();
           listaActividades = control.listarGasto();
           
           table.addColumn("Nombre Actividad");
           table.addColumn("Valor");
           table.addColumn("Categoria");
           table.addColumn("Fecha");
           table.setRowCount(listaActividades.size());
           
           int i=0;
           
           for(ActividadVO x:listaActividades)
           {
               table.setValueAt(x.getNombreGasto(), i, 0);
               table.setValueAt(x.getValorGasto(), i, 1);
               table.setValueAt(x.getCategoriaGasto(), i, 2);
               table.setValueAt(x.getFechaGasto(), i, 3);
               i++;
           }
           
           this.tblAllGastos.setModel(table);
           
       } 
       catch (Exception ex) {
           Logger.getLogger(GastoGUI.class.getName()).log(Level.SEVERE, null, ex);
       }
    }
    
    public void calcularGastos(String date)
    {
           int gastoTotal;
           int gastoTotalFecha;
           int gastoAcumulado;
           
           gastoTotal= control.enviarFechaInt(date);
           lblTotalGastadoDia.setText(String.valueOf(gastoTotal));
           
           gastoTotalFecha = control.enviarFechaIntGT(date);
           lblShowTotalMes.setText(String.valueOf(gastoTotalFecha));
           
           gastoAcumulado = control.enviarFechaIntAcum(date);
           lblShowAcumulado.setText(String.valueOf(gastoAcumulado));
    }
    
