    public void listarCategoria()
    {
        try {
            
            String seleccion = "Seleccione:";
            
           DefaultComboBoxModel combo = new DefaultComboBoxModel();
           ArrayList<CategoriaVO> listaCategoria = new ArrayList();
           combo.insertElementAt(seleccion, 0);
           listaCategoria = control.listarCategorias();
           
           for(CategoriaVO x:listaCategoria)
           {
              combo.insertElementAt(x.getCategoriaName(), x.getIdCategoria());
           }
           
           this.cbxCategoria.setModel(combo);
           
       } catch (ClassNotFoundException | SQLException ex) {
           Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
       }
    }
