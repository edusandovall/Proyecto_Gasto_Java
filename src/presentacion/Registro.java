    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    
           String nombreGasto = txtNombreGasto.getText();
           int valorGasto = Integer.parseInt(txtValor.getText());
           String fecha = ((JTextField)jdcFecha.getDateEditor().getUiComponent()).getText();
           int idCategoria = cbxCategoria.getSelectedIndex();
           
           if(idCategoria==0)
           {
               JOptionPane.showMessageDialog(null, "Por favor seleccione una categoria", "Mensaje", 1);
           }
           else if(control.enviarDatos(nombreGasto, valorGasto, fecha, idCategoria)==true)
           {
               JOptionPane.showMessageDialog(null, "Ingreso Exitoso", "Mensaje", 1);
               limpiar();
               
               modGasto.listarActividades();
               gasto.listarActividades();
           }
           else
           {
               JOptionPane.showMessageDialog(null, "Ha ocurrido un error", "Mensaje", 2);
               limpiar();
           }
    }//GEN-LAST:event_btnGuardarActionPerformed

