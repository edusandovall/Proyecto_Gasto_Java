    private void tblGastosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGastosMouseClicked
       
        int row = tblGastos.getSelectedRow();
        String clickTable = (tblGastos).getModel().getValueAt(row, 0).toString();
        
        ArrayList<ActividadVO> listaActividades= new ArrayList();
        listaActividades = control.gastoPorID(Integer.parseInt(clickTable));
        cbxCategoria.removeAllItems();
        
        for(ActividadVO x:listaActividades)
        {
            lblShowId.setText(String.valueOf(x.getIdGasto()));
            txtNombreGasto.setText(x.getNombreGasto());
            txtValorGasto.setText(String.valueOf(x.getValorGasto()));
            cbxCategoria.addItem(x.getCategoriaGasto());
            ((JTextField)jdcFechaSeleccion.getDateEditor().getUiComponent()).setText(x.getFechaGasto().substring(0, 10 ));
        }
    }//GEN-LAST:event_tblGastosMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        int id;
        String nombre;
        int valor;
        int categoria;
        String fecha;
        
        id = Integer.parseInt(lblShowId.getText());
        nombre = txtNombreGasto.getText();
        valor = Integer.parseInt(txtValorGasto.getText());
        categoria = cbxCategoria.getSelectedIndex();
        fecha = ((JTextField)jdcFechaSeleccion.getDateEditor().getUiComponent()).getText();
        
        if(control.actualizarDatos(id, nombre, valor, fecha, categoria)==true)
        {
            JOptionPane.showMessageDialog(null, "El gasto ha sido actualizado con exito", "Mensaje", 1);
            listarActividades();
            gasto.listarActividades();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No se ha podido actualizar el gasto", "Mensaje", 0);
        }
        
    }//GEN-LAST:event_btnActualizarActionPerformed

