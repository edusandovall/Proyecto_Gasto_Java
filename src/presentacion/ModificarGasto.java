    public void listarActividades()
    {
       try 
       {
           DefaultTableModel table = new DefaultTableModel();
           ArrayList<ActividadVO> listaActividades= new ArrayList();
           listaActividades = control.listarGasto();
           
           table.addColumn("Id Gasto");
           table.addColumn("Nombre Actividad");
           table.addColumn("Valor");
           table.addColumn("Categoria");
           table.addColumn("Fecha");
           table.setRowCount(listaActividades.size());
           
           int i=0;
           
           for(ActividadVO x:listaActividades)
           {
               table.setValueAt(x.getIdGasto(), i, 0);
               table.setValueAt(x.getNombreGasto(), i, 1);
               table.setValueAt(x.getValorGasto(), i, 2);
               table.setValueAt(x.getCategoriaGasto(), i, 3);
               table.setValueAt(x.getFechaGasto(), i, 4);
               i++;
           }
           
           this.tblGastos.setModel(table);
           
       } 
       catch (Exception ex) {
           Logger.getLogger(GastoGUI.class.getName()).log(Level.SEVERE, null, ex);
       }
    }
    
